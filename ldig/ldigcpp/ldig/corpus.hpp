	CorpusFactory(const size_t div, const size_t test) : n_div(div), n_test(test) {
		if (n_div <= n_test)
				throw Exception("need [div] > [test]");
	}

	size_t size() const { return N; }

	void load(const std::vector<std::string> &files) {
		const size_t BUFSIZE = 4096;
		char buf[BUFSIZE];
		for (std::vector<std::string>::const_iterator i=files.begin(), ie=files.end();i!=ie;++i) {
			std::ifstream ifs(*i);
			if (!ifs.is_open()) {
				throw Exception("cannot open the file");
			}
			size_t n = 0;
			while (!ifs.eof()) {
				ifs.getline(buf, BUFSIZE);
				std::string line(buf);
				++n;

				std::smatch m;
				if (std::regex_match(line, m, REXLINE)) {
					const std::string& label = m[2].str();
					dataset[label].push_back(TextLine(line, filenames.size(), n));
				}
			}
			filenames.push_back(*i);
			std::cout << "loaded... " << *i << " : " << n << " lines " << std::endl;
		}

		N = 0;
		for (auto i=dataset.begin(), ie=dataset.end();i!=ie;++i) {
			if (i->second.size() < n_div)
				throw Exception("all labels need more than [div] texts.");
			std::random_shuffle(i->second.begin(), i->second.end());
			N += i->second.size();
		}
	}

	void sortDataset() {
		for (auto i=dataset.begin(), ie=dataset.end();i!=ie;++i) {
			std::sort(i->second.begin(), i->second.end(), 
				[](const TextLine &a, const TextLine &b)-> bool {
					if (a.filename < b.filename) return true;
					if (a.filename == b.filename && a.lineno < b.lineno) return true;
					return false;
				}
			);
		}
	}

	bool next() {
		if (combination.size()==0) {
			for (size_t i=0;i<n_test;++i) combination.push_back(n_test-1-i);
		} else {
			size_t upper = n_div-1;
			for(auto i=combination.begin(), ie=combination.end();i!=ie;++i) {
				if (*i<upper) {
					++(*i);
					while(i!=combination.begin()) {
						--i;
						*i = *(i+1) + 1;
					}
					break;
				}
				--upper;
			}
			if (upper + n_test + 1 == n_div) return false;
		}
		
		train.clear();
		for (auto i=dataset.begin(), ie=dataset.end();i!=ie;++i) {
			const size_t M = i->second.size();
			for (size_t j=0;j<i->second.size();++j) {
				if (std::find(combination.begin(), combination.end(), j * n_div / M)==combination.end()) {
					std::smatch m;
					if (std::regex_match(i->second[j].line, m, REXLINE))
						train.addText(i->first, cybozu::String(m[4].str()));
				}
			}
		}
		train.postLoad();

		test_iterator = dataset.begin();
		test_index = -1;
		return true;
	}

	bool nexttest() {
		if (test_iterator==dataset.end()) return false;
		++test_index;
		while (true) {
			size_t M = test_iterator->second.size();
			if (test_index>=M) {
				++test_iterator;
				test_index = 0;
				if (test_iterator==dataset.end()) return false;
				continue;
			}

			size_t m = test_index * n_div / M;
			if (std::find(combination.begin(), combination.end(), m)==combination.end()) {
				test_index = ((m + 1) * M + n_div - 1) / n_div;
				continue;
			}

			return true;
		}
	}

	const std::string &testlabel() const {
		return test_iterator->first;
	}

	cybozu::String testtext() const {
		std::smatch m;
		if (std::regex_match(test().line, m, REXLINE)) {
			cybozu::String s;
			normalize(s, m[4].str());
			cybozu::String st(LF);
			st.append(s);
			st.append(LF);
			return st;
		}
		throw Exception("bug?");
	}

	TextLine &test() const {
		return test_iterator->second.at(test_index);
	}

	size_t n_test_for_block() const {
		size_t num = 1, den = 1;
		for (size_t n = n_div - 1, r = n_test - 1;r>0;--n,--r) {
			num *= n;
			den *= r;
		}
		return num / den;
	}

	void output(std::ofstream &os, const unsigned int score) const {
		os << "********* score : " << score << " **********" << std::endl;
		for (auto i=dataset.begin(), ie=dataset.end();i!=ie;++i) {
			for (auto j=i->second.begin(), je=i->second.end();j!=je;++j) {
				if (j->score == score) {
					os << filenames[j->filename] << "(" << (unsigned int)j->lineno << ") : " << j->line << std::endl;
				}
			}
		}
		os << std::endl;
	}
