void normalize(cybozu::String &result, const cybozu::String &org_text) {
	cybozu::String st = cybozu::regex_replace(org_text, REXHTTP, "");
	st = cybozu::regex_replace(st, REXFACE, " ");
	st = cybozu::regex_replace(st, REXNUMBER, "0");

	cybozu::String text;
	text.reserve(st.size());
	for (cybozu::String::const_iterator i=st.begin(), ie=st.end();i!=ie;++i) {
		cybozu::Char x = *i;
		if (x<=0x1f) {
			x = ' ';
		/*} else if (x<=0x39) {
			if (x>=0x30) x = '0';*/
		} else if (x<=0x5a) {
			if (x>=0x41 && x!=0x49) x += 0x20;
		} else if (x<=0xa0) {
			if (x>=0x7f) x = ' ';
		} else if (x<=0xde) {
			if (x>=0xc0) {
				if (x!=0xd7) x += 0x20;
			} else if (x==0xb4) x = 0x27;
		} else if (x<=0x4ff) {
			if (x<=0x136) {
				if (x>=0x100 && (x & 1) == 0 && x != 0x130) ++x;
			} else if (x<=0x147) {
				if (x>=0x139 && (x & 1) == 1) ++x;
			} else if (x<=0x176) {
				if (x>=0x14a && (x & 1) == 0) ++x;
			} else if (x<=0x17d) {
				if (x==0x178) {
					x = 0xff;
				} else if (x>=0x179 && (x & 1) == 1) ++x;
			} else if (x==0x17f) {
				x = 's';  // long s
			} else if (x<=0x24f) {
				if (x==0x1a0 || x==0x1af) {  // for Vietnamese
					++x;
				} else if (x>=0x1f8 && x<=0x232) {
					if ((x & 1) == 0) ++x;
					if (x==0x219) {  // for Romanian
						x=0x15f;
					} else if (x==0x21b) {
						x=0x163;
					}
				}
			} else if (x<=0x323) {
				if (x==0x30c) {
					auto pre = text.end() - 1;
					if (*pre == 'c') {
						*pre = 0x10d;
						continue;
					} else if (*pre == 's') {
						*pre = 0x161;
						continue;
					} else if (*pre == 'z') {
						*pre = 0x17e;
						continue;
					}
				} else if (x>=0x300 && replace_vietnamese(text, x)) continue;
			} else if (x<=0x3aa) {
				if (x>=0x391 && x!=0x3a2) x += 0x20;  // Greek lowerize
			} else if (x>=0x400) {  // Cyrillic lowerize
				if (x<=0x40f) {
					x += 0x50;  // 0400-040f => 0450-045f
				} else if (x<=0x42f) {
					x += 0x20;  // 0410-042f => 0430-044f
				} else if (x<=0x4bf) {
					if (x>=0x48a && (x & 1)==0) ++x;  // 048a-04bf
				}
			}
		} else if (x<=0x1ef9) {
			if (x==0x6cc) {
				x = 0x64a;  // Farsi yeh => Arabic yeh
			} else if (x>=0x1ea0 && (x & 1) == 0) ++x;  // for Vietnamese
		} else if (x<=0x206f) {
			if (x>=0x2000) {
				if (x<=0x2015) {
					x = (x<=0x200f)?' ':'-';
				} else if (x==0x2018 || x==0x2019) {
					x = 0x0027; // apostrophe
				}
			}
		} else if (x<=0x24e9) {
			if (x>=0x24b6) x = ((x-0x24b6) % 26) + 'a';
		} else if (x<=0x3096) {
			if (x==0x3000) {
				x = ' ';
			} else if (x>=0x3041) {
				x = 0x3042;    // HIRAGANA 'A'
			}
		} else if (x<=0x30fa) {
			if (x>=0x30a1) {
				x = 0x30a2;    // KATAKANA 'A'
			}
		} else if (x<=0x30fa) {
			if (x>=0x30a1) {
				x = 0x30a2;    // KATAKANA 'A'
			}
		} else if (x<=0x9fcb) {
			if (x>=0x4e00) {
				unsigned char c = kanji_table[x - 0x4e00];
				if ( c < sizeof(kanji_replace) / sizeof(kanji_replace[0])) {
					x = kanji_replace[c];
				}
			}
		} else if (x<=0xd7af) {
			if (x>=0xac00) {
				x = 0xc774;    // hangeul
			}
		} else if (x<=0xff5e) {
			if (x>=0xff01) {
				x -= 0xff00-0x20;
				if (x>=0x30 && x<=0x39) x = '0';
			} else if (x==0xff5e) x = 0x301c;	// FULLWIDTH TILDE => WAVE DASH
		} else if (x>=0x10000) {
			x = ' ';
		}

		auto j = text.end();
		if (text.length() < 2 || *(j-1) != x || *(j-2) != x) text += x;

	}
	result = cybozu::regex_replace(text, REXLAUGH, "$1$2$1$2");
}


class Corpus {
	size_t totalN, maxN;
	cybozu::String fulltext;
	std::vector<TextVec> texts_;
	std::unordered_map<std::string, size_t> labels_;

public:
	const cybozu::String& text() const { return fulltext; }
	const std::vector<TextVec>& texts() const { return texts_; }
	const std::unordered_map<std::string, size_t> &labels() const { return labels_; }
	size_t size() const { return totalN; }
	size_t maxsize() const { return maxN; }

	Corpus() : totalN(0), maxN(0), fulltext(LF) {
	}

	void clear() {
		totalN = maxN = 0;
		fulltext = LF;
		texts_.clear();
		labels_.clear();
	}

	void load(const std::vector<std::string> &files) {
		for (std::vector<std::string>::const_iterator i=files.begin(), ie=files.end();i!=ie;++i) {
			std::cout << "loading... " << *i << std::endl;
			try {
				cybozu::Mmap map(*i);
				//cybozu::Utf8ref ref(map.get(), map.size());
				//cybozu::String s(map.get(), (cybozu::String::size_type)map.size());

				const char *p = map.get();
				const char *end = p + map.size();
				loadOne(p, end);

			} catch (std::exception& e) {
				printf("%s\n", e.what());
			}

		}
		postLoad();
	}

	void loadOne(const char *begin, const char *end) {
		std::regex_iterator<const char*> i( begin, end, REXLINE ), iend;
		for (; i != iend; ++i) {
			const std::string& label = (*i)[2].str();
			const cybozu::String org_text((*i)[4].str());
			addText(label, org_text);
		}
	}

	void addText(const std::string &label, const cybozu::String &org_text) {
		cybozu::String text;
		normalize(text, org_text);

		auto i = labels_.find(label);
		size_t j;
		if (i==labels_.end()) {
			labels_[label] = j = texts_.size();
			texts_.push_back(TextVec(label));
		} else {
			j = i->second;
		}
		TextVec &vec = texts_[j];

		size_t begin = fulltext.size() - 1;
		fulltext += text;
		fulltext += LF;
		vec.vec.push_back(TextPos(begin, text.size() + 2));
	}

	void postLoad() {
		std::sort(texts_.begin(), texts_.end(), 
			[](const TextVec &x, const TextVec &y){ return x.label < y.label; });
		labels_.clear();
		for (size_t i=0;i<texts_.size();++i) {
			labels_[texts_[i].label] = i;
			size_t n = texts_[i].vec.size();
			totalN += n;
			if (maxN < n) maxN = n;
		}
	}
};

