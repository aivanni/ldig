bool replace_vietnamese(cybozu::String &text, const int x) {
	for(size_t d=0;d<D;++d) {
		if (diacriticals[d]==x) {
			auto pre = text.end() - 1;
			int c = *pre;
			if (c=='I') c='i';
			for(size_t v=0;v<V;++v) {
				if (vowels[v]==c) {
					*pre = VIETNAMESES[d*V+v];
					return true;
				}
			}
			break;
		}
	}
	return false;
}

